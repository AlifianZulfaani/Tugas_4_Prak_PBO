    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String id = TFID.getText().toString().trim();
        String username = TFUsername.getText().toString().trim();
        String password = PFPassword.getText().toString().trim();
        
        if (password.equals("") || username.equals("")){
            JOptionPane.showMessageDialog(null, "Username dan Password tidak boleh kosong");
        }else{
            try{
                Connection cnc = Koneksi.getConnect();
                String sql = "INSERT INTO users VALUES ( ?, ?, ?)";
                PreparedStatement pps = cnc.prepareStatement(sql);
                pps.setString(1, id); pps.setString(2, username); pps.setString(3, password);
                pps.executeUpdate();
                pps.close();
                JOptionPane.showMessageDialog(null, "Berhasil Buat Akun");
            }catch(SQLException exc){
                System.out.println("Ada Error di Script btnRegist");
            }finally{
                this.dispose();
                Login lgn = new Login();
                lgn.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

