    private void autoID(){
        try{
            Connection cnc = Koneksi.getConnect();
            Statement stm = cnc.createStatement();
            String sql = "SELECT * FROM users ORDER BY id DESC";
            ResultSet rsl = stm.executeQuery(sql);
            if (rsl.next()) {
                String NoID = rsl.getString("id").substring(2);
                String ID = "" +(Integer.parseInt(NoID)+1);
                String Nol = "";
                
                if (ID.length()==1) 
                {Nol = "00";}
                else if(ID.length()==2)
                {Nol = "0";}
                else if(ID.length()==3)
                {Nol = "";}
                
                TFID.setText(Nol + ID);
            }else{
                TFID.setText("001");
            }
            rsl.close();
            stm.close();
        }catch(Exception exc){
            System.out.println("Ada Error di Script AutoID");
        }
    }

